// Sistema de internacionalização
class I18n {
    constructor() {
        this.currentLang = localStorage.getItem('mariachi_lang') || 'es';
        this.translations = translations;
    }
    
    setLanguage(lang) {
        if (this.translations[lang]) {
            this.currentLang = lang;
            localStorage.setItem('mariachi_lang', lang);
            this.updatePageTranslations();
            document.documentElement.lang = lang;
        }
    }
    
    t(key) {
        return this.translations[this.currentLang][key] || key;
    }
    
    updatePageTranslations() {
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.t(key);
            
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        });
        
        // Trigger custom event for dynamic content
        document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: this.currentLang } }));
    }
}

// Inicializar
const i18n = new I18n();

// Exponer al global para app-premium.js
window.translations = translations;
window.i18n = i18n;

// Aplicar traducciones iniciales al cargar DOM
document.addEventListener('DOMContentLoaded', () => {
    i18n.updatePageTranslations();
});
